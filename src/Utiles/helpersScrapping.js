function delay(ms) {
  return new Promise((resolve) => setTimeout(resolve, ms));
}

// Función para simular comportamiento humano con delays variables
function humanDelay() {
  const baseDelay = Math.random() * 3000 + 2000; // 2-5 segundos base
  const jitter = Math.random() * 2000; // 0-2 segundos de variación
  return baseDelay + jitter;
}

// Función para generar headers más realistas
function getRealisticHeaders(referer = null) {
  const userAgent = getRandomUserAgent();
  const isMobile =
    userAgent.includes("Mobile") ||
    userAgent.includes("Android") ||
    userAgent.includes("iPhone");

  const headers = {
    "User-Agent": userAgent,
    Accept:
      "text/html,application/xhtml+xml,application/xml;q=0.9,image/avif,image/webp,image/apng,*/*;q=0.8,application/signed-exchange;v=b3;q=0.7",
    "Accept-Language": "es-AR,es;q=0.9,en;q=0.8",
    "Accept-Encoding": "gzip, deflate, br",
    DNT: "1",
    Connection: "keep-alive",
    "Upgrade-Insecure-Requests": "1",
    "Sec-Fetch-Dest": "document",
    "Sec-Fetch-Mode": "navigate",
    "Sec-Fetch-Site": referer ? "same-origin" : "none",
    "Sec-Fetch-User": "?1",
    "Cache-Control": "max-age=0",
    "sec-ch-ua":
      '"Not_A Brand";v="8", "Chromium";v="120", "Google Chrome";v="120"',
    "sec-ch-ua-mobile": isMobile ? "?1" : "?0",
    "sec-ch-ua-platform": '"macOS"',
  };

  if (referer) {
    headers["Referer"] = referer;
  }

  // Agregar cookies básicas para parecer más realista
  headers["Cookie"] = "nav_rdc=MLA; nav_rdc=MLA; nav_rdc=MLA";

  return headers;
}

function extractItemIdFromUrl(url) {
  const m1 = url.match(/MLA-(\d+)/); // https://…/MLA-123456…
  if (m1) return `MLA${m1[1]}`;

  const m2 = url.match(/\/p\/(MLA\d+)/); // https://…/p/MLA123456
  if (m2) return m2[1];

  const m3 = url.match(/[?&]item_id=(MLA\d+)/);
  if (m3) return m3[1];

  return null;
}

